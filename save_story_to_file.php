<?php
// **هشدار امنیتی:**
// این کد بسیار ساده است و برای استفاده در محیط واقعی مناسب نیست.
// حتماً ورودی کاربر را پاکسازی و اعتبارسنجی کنید!

$story = $_POST['story'];

// **پاکسازی ورودی (حداقلی):**
$story = htmlspecialchars($story, ENT_QUOTES, 'UTF-8');  // جلوگیری از XSS

$filename = 'uploaded_stories.txt';  // نام فایلی که داستان‌ها ذخیره می‌شوند

// ذخیره داستان در فایل
if (file_put_contents($filename, "==== شروع داستان ====\n" . $story . "\n==== پایان داستان ====\n\n", FILE_APPEND)) {
    echo "داستان با موفقیت ذخیره شد.";
} else {
    echo "خطا در ذخیره داستان.";
}
?>